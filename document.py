import mathpack
import math


print("mathpack.ceil(3.14) ==", mathpack.ceil(3.14))
print("math.ceil(3.14) ==", math.ceil(3.14))
print()

print("mathpack.comb(6, 3) ==", mathpack.comb(6, 3))
print("math.comb(6, 3) ==", math.comb(6, 3))
print()

print("mathpack.fabs(-3) ==", mathpack.fabs(-3))
print("math.fabs(-3) ==", math.fabs(-3))
print()

print("mathpack.factorial(10) ==", mathpack.factorial(10))
print("math.factorial(10) ==", math.factorial(10))
print()

print("mathpack.fmod(3.14, 1.3) ==", mathpack.fmod(3.14, 1.3))
print("math.fmod(3.14, 1.3) ==", math.fmod(3.14, 1.3))
